#include<iostream>

int main()
{
	int Num1{20}, Num2{12};

	std::cout<<Num1 <<" equal to "<<Num2<<" : "<<(Num1 == Num2)<<std::endl;
	std::cout<<Num1 <<" equal to "<<Num2<<" : "<<(Num1 != Num2)<<std::endl;
	std::cout<<Num1 <<" equal to "<<Num2<<" : "<<(Num1 <  Num2)<<std::endl;
	std::cout<<Num1 <<" equal to "<<Num2<<" : "<<(Num1 >  Num2)<<std::endl;
	std::cout<<Num1 <<" equal to "<<Num2<<" : "<<(Num1 <= Num2)<<std::endl;
	std::cout<<Num1 <<" equal to "<<Num2<<" : "<<(Num1 >= Num2)<<std::endl;





	return 0;
}